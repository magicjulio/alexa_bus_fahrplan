class CatchAllExceptionHandler(AbstractExceptionHandler):
    """Generic error handling to capture any syntax or routing errors. If you receive an error
    stating the request handler chain is not found, you have not implemented a handler for
    the intent being invoked or included it in the skill builder below.
    """
    def can_handle(self, handler_input, exception):
        # type: (HandlerInput, Exception) -> bool
        return True

    def handle(self, handler_input, exception):
        # type: (HandlerInput, Exception) -> Response
        logger.error(exception, exc_info=True)

        speak_output = "Sorry, I had trouble doing what you asked. Please try again."

        return (
            handler_input.response_builder
                .speak(speak_output)
                .ask(speak_output)
                .response
        )

class planintentHandler(AbstractRequestHandler):

    def can_handle(self, handler_input):
        return ask_utils.is_intent_name("planintent")(handler_input)
        
    def handle(self, handler_input):
        
        bus_times = ['06:50', '07:13', '07:22', '07:25', '07:51', '08:06', '08:21', '08:51', '09:21', '09:51', '10:21', '10:51', '11:21', '11:44', '11:51', '12:21', '12:45', '12:51', '13:22', '13:42', '13:51', '14:22', '14:51', '15:22', '15:32', '15:51', '16:22', '17:01', '17:22', '17:51', '18:21', '18:51', '19:21', '19:51', '20:45', '21:38', '22:53', '23:53']

        # Get the current time workaround timezone dillema by just adding 2 hours to the time
        current_time = datetime.now() + timedelta(hours=2)
        
        
        # Find the next bus time after the current time
        next_bus_time = None
        for bus_time in bus_times:
            
            # convert string to time obj
            bus_time_obj = datetime.strptime(bus_time, "%H:%M")
            
            # Adjust the bus time to today's date (might be buggy near next day)
            bus_time_today = bus_time_obj.replace(year=current_time.year, month=current_time.month, day=current_time.day)
            
            # Check if this bus time is after the current time
            if current_time < bus_time_today:
                next_bus_time = bus_time_today
                break
        
        # Calculate the time difference in minutes
        if next_bus_time:
            time_difference = next_bus_time - current_time
            minutes_left = time_difference.total_seconds() / 60  # Convert seconds to minutes
            speak_output = f"Der nächste Bus kommt in {minutes_left:.0f} Minuten" + f", um {next_bus_time.strftime('%H:%M')}. Viel Glück"
        else:
            speak_output = "Heute fährt kein Bus mehr"

        return handler_input.response_builder.speak(speak_output).response
            
            
    
